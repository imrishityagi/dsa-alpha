public class javaFor {
    public static void main(String[] args) {
        // for loop - syntax
        // for(initialisation; condition; updation) {

        // }

        // print numbers 1 to 10 using for loop
        for(int i = 1; i <= 10; i++) {
            System.out.println("Hello World");
        }

        // print square pattern
        for(int i = 1; i < 4; i++) {
            System.out.println("* * * *");
        }
    }
}
